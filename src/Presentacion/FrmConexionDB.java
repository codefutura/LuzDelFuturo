    private void tbnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnGuardarActionPerformed
    
    if(String.valueOf(tClave.getPassword()).isEmpty()){
        JOptionPane.showMessageDialog(this,"Ingresa la contraseña");
        tClave.requestFocus();
        return;
    }
        
    String datos=(new StringBuilder()).append(tServidor.getText()).append(";")
            .append(tBaseDatos.getText()).append(";").append(String.valueOf(tClave.getPassword()))
            .append(";").append(tPuerto.getText()).append(";")
            .append(tUser.getText()).toString();
    
    ConectarBd conex =new ConectarBd(0);
    if(conex.probarConexion(tServidor.getText(),tBaseDatos.getText(),tPuerto.getText(),tUser.getText(),String.valueOf(tClave.getPassword()),this)==true){
           JOptionPane.showMessageDialog(this,"Conexion exitosa");
       }else{
        return;
    }
    setGuarda(true);
    setFichero(); 
    ConectarBd.asignarParametrosCon(tServidor.getText(),tBaseDatos.getText(), tPuerto.getText(), tUser.getText(), String.valueOf(tClave.getPassword()));
    dispose();
    
    }//GEN-LAST:event_tbnGuardarActionPerformed

