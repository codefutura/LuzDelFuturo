   public void leerArch() {
        File archivo;
        archivo= new File("database.properties");      
          if(archivo.exists()==false){
                return;
        }    
        try {
            Properties pro;
            pro = new Properties();
            pro.load(new FileReader("database.properties"));
            tBaseDatos.setText(pro.getProperty("db.database"));
            tClave.setText(pro.getProperty("db.clave"));
            tPuerto.setText(pro.getProperty("db.puerto"));
            tUser.setText(pro.getProperty("db.usuario"));
            tServidor.setText(pro.getProperty("db.ip")); 
           
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmConexionDB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmConexionDB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrmConexionDB.class.getName()).log(Level.SEVERE, null, ex);
        }    
  }
   
   public void crearFichero(String datos, String senda) {
        try{ 
            FileWriter fw   = new FileWriter(senda, false);
            fw.close(); 
        } catch (IOException e) {
		    JOptionPane.showMessageDialog(this, "Error Al registrar la conexion "+e.getMessage()); }
    }
    
    public void setFichero() {
        try {
            File archivo;
            archivo= new File("database.properties");      
            if(archivo.exists()==false){
                 crearFichero("" ,"database.properties");
            }
            Properties pro;
            pro = new Properties();
            pro.load(new FileReader("database.properties"));
            pro.setProperty("db.database", tBaseDatos.getText());
            pro.setProperty("db.clave", String.valueOf(tClave.getPassword()));
            pro.setProperty("db.puerto", tPuerto.getText());
            pro.setProperty("db.usuario", tUser.getText());
            pro.setProperty("db.ip", tServidor.getText());
            pro.store(new FileWriter("database.properties"), "Configuracion base de datos");
            pro=null;
                     
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmConexionDB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmConexionDB.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
