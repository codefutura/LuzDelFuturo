    private int getNumeroMes() {
        Calendar c = Calendar.getInstance();
        return c.get(Calendar.MONTH);
    }
    
    private void cargarDatos() {
        int anio = FormatoFecha.getAnio(tFecha);
        ArrayList datos = DbEmpleado.getEmpleadosEnNomina(cbMes.getSelectedIndex() + 1, anio);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        datos.forEach(obj -> {
            model.addRow((Object[]) obj);
        });
    }
    
    public void limpiarTabla(JTable tabla) {
        try {
            DefaultTableModel model = (DefaultTableModel) tabla.getModel();
            int filas = tabla.getRowCount();
            for (int i = 0; filas > i; i++) {
                model.removeRow(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al limpiar la tabla.");
        }
    }
    
    private double redondearDecimales(double valorInicial, int numeroDecimales) {
        double parteEntera, resultado;
        resultado = valorInicial;
        parteEntera = Math.floor(resultado);
        resultado = (resultado - parteEntera) * Math.pow(10, numeroDecimales);
        resultado = Math.round(resultado);
        resultado = (resultado / Math.pow(10, numeroDecimales)) + parteEntera;
        return resultado;
    }
    
    private void calcularTotales() {
        double total = 0;
        double totDeduccion = 0;
        for (int x = 0; x < jTable1.getRowCount(); x++) {
            try {
                total += Double.parseDouble(String.valueOf(jTable1.getValueAt(x, 4)));
            } catch (NumberFormatException e) {
            }
            try {
                totDeduccion += Double.parseDouble(String.valueOf(jTable1.getValueAt(x, 3)));
            } catch (NumberFormatException e) {
            }
            


        }
        
        tDeduciones.setValue(totDeduccion);
        tTotal.setValue(total);
        
    }
    
