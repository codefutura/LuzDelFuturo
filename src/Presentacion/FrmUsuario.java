    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if((String.valueOf(tClave.getPassword()).isEmpty()||String.valueOf(tReClave.getPassword()).isEmpty())||tNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Favor rellenar todos los campos");
            return;
        }
        
        if(String.valueOf(tClave.getPassword()).isEmpty() ? String.valueOf(tReClave.getPassword()) != null : !String.valueOf(tClave.getPassword()).equals(String.valueOf(tReClave.getPassword())))
        {
            JOptionPane.showMessageDialog(this,"Favor confirmar la contrase침a");
            return;
        }

        u.setNombre(tNombre.getText());
        u.setClave(String.valueOf(tClave.getPassword()));
    
        if(idUser>0){
            dbu.modificarUsuario(u, idUser);
        }else{
            dbu.crearUsuario(u);
        }
        
        this.aceptada=true;

        this.dispose();

    }//GEN-LAST:event_btnGuardarActionPerformed

