    private void btnModificaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificaActionPerformed

        int id=0;
        try {
            id=Integer.parseInt(String.valueOf(jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 0)));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
     
        FrmUsuario mUsuario=new FrmUsuario(this,true,id);
        mUsuario.pack();
        mUsuario.setLocationRelativeTo(null);
        mUsuario.cargarUsuario();
        mUsuario.setVisible(true);
       
        if(mUsuario.isAceptada()){ 
          limpiarTabla(jTableUsuarios);
          DbUsuario.buscarPorNombre((DefaultTableModel) jTableUsuarios.getModel(), tBuscar.getText(),this);
        }
    }//GEN-LAST:event_btnModificaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        if(JOptionPane.showConfirmDialog(this, "Estas seguro que deseas eliminar producto seleccionado...",
            "Eliminar producto...", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
        int id=0;
        try {
            id=Integer.parseInt(String.valueOf(jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 0)));
        } catch (NumberFormatException e) {
        }
        
        DbUsuario.eliminarUsuario(id);
    
        jTableUsuarios.remove(jTableUsuarios.getSelectedRow());
        
      

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

